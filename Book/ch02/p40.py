num1 = 100
num2 = 20

# 동등비교
bool_result = num1 == num2
print(bool_result)
bool_result = num1 != num2
print(bool_result)

# 크기비교
bool_result = num1 > num2
print(bool_result)
bool_result = num1 >= num2
print(bool_result)
bool_result = num1 < num2
print(bool_result)
bool_result = num1 <= num2
print(bool_result)